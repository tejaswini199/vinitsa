<strong>1979</strong> - Started a preparatory faculty for teaching 230 foreign citizens.
</li>
<li><i class="fa fa-dot-circle-o" aria-hidden=true></i>
<strong>1979</strong> - The opening of the Faculty of doctor’s advanced training and its branch.
</li>
